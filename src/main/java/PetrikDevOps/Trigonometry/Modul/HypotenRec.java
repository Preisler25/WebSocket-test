package PetrikDevOps.Trigonometry.Modul;

public class HypotenRec {
    private float length;
    private float angle;

    public HypotenRec(float length, float angle) {
        this.length = length;
        this.angle = angle;
    }

    public float getLength() {
        return length;
    }


    public float getAngle() {
        return angle;
    }

}
